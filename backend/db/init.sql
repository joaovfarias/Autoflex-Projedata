SET SQLBLANKLINES ON;

ALTER SESSION SET CONTAINER = XEPDB1;
ALTER SESSION SET CURRENT_SCHEMA = SYSTEM;

CREATE TABLE PRODUCT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE VARCHAR2(50) NOT NULL UNIQUE,
    P_NAME VARCHAR2(100) NOT NULL,
    P_VALUE NUMBER(10,2) NOT NULL
);

CREATE TABLE RAW_MATERIAL (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE VARCHAR2(50) NOT NULL UNIQUE,
    R_NAME VARCHAR2(100) NOT NULL,
    STOCK_QUANTITY NUMBER NOT NULL
);

CREATE TABLE PRODUCT_RAW_MATERIAL (
    PRODUCT_ID NUMBER NOT NULL,
    RAW_MATERIAL_ID NUMBER NOT NULL,
    REQUIRED_QUANTITY NUMBER NOT NULL,
    CONSTRAINT PK_PRODUCT_RAW 
        PRIMARY KEY (PRODUCT_ID, RAW_MATERIAL_ID),
    CONSTRAINT FK_PRODUCT 
        FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(ID),
    CONSTRAINT FK_RAW_MATERIAL 
        FOREIGN KEY (RAW_MATERIAL_ID) REFERENCES RAW_MATERIAL(ID)
);